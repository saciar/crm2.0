package crm.gui.tablerenderer.abms;

public class BusquedaProvinciasItem {
	private String idProvincia;
	private String nombrePais;
	private String nombreProvincia;
	private String codigo;
	/**
	 * @return Returns the codigo.
	 */
	public String getCodigo() {
		return codigo;
	}
	/**
	 * @param codigo The codigo to set.
	 */
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	/**
	 * @return Returns the nombreProvincia.
	 */
	public String getNombreProvincia() {
		return nombreProvincia;
	}
	/**
	 * @param nombreProvincia The nombreProvincia to set.
	 */
	public void setNombreProvincia(String nombreProvincia) {
		this.nombreProvincia = nombreProvincia;
	}
	/**
	 * @return Returns the codigo.
	 */
	public String getIdProvincia() {
		return idProvincia;
	}
	/**
	 * @param codigo The codigo to set.
	 */
	public void setIdProvincia(String codigo) {
		this.idProvincia = codigo;
	}
	/**
	 * @return Returns the nombreFantasia.
	 */
	public String getNombrePais() {
		return nombrePais;
	}
	/**
	 * @param nombreFantasia The nombreFantasia to set.
	 */
	public void setNombrePais(String nombreFantasia) {
		this.nombrePais = nombreFantasia;
	}
}
