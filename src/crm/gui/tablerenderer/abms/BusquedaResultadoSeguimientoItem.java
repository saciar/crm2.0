package crm.gui.tablerenderer.abms;

public class BusquedaResultadoSeguimientoItem {
	private String idResultado;
	private String nombreAccion;
	private String nombreResultado;
	private String codigo;
	/**
	 * @return Returns the codigo.
	 */
	public String getCodigo() {
		return codigo;
	}
	/**
	 * @param codigo The codigo to set.
	 */
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	/**
	 * @return Returns the nombreProvincia.
	 */
	public String getNombreResultado() {
		return nombreResultado;
	}
	/**
	 * @param nombreProvincia The nombreProvincia to set.
	 */
	public void setNombreResultado(String nombreProvincia) {
		this.nombreResultado = nombreProvincia;
	}
	/**
	 * @return Returns the codigo.
	 */
	public String getIdResultado() {
		return idResultado;
	}
	/**
	 * @param codigo The codigo to set.
	 */
	public void setIdResultado(String codigo) {
		this.idResultado = codigo;
	}
	/**
	 * @return Returns the nombreFantasia.
	 */
	public String getNombreAccion() {
		return nombreAccion;
	}
	/**
	 * @param nombreFantasia The nombreFantasia to set.
	 */
	public void setNombreAccion(String nombreFantasia) {
		this.nombreAccion = nombreFantasia;
	}
}
