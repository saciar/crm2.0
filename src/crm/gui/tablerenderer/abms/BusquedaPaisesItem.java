package crm.gui.tablerenderer.abms;

public class BusquedaPaisesItem {
	private String codigo;
	private String nombrePais;
	/**
	 * @return Returns the codigo.
	 */
	public String getCodigo() {
		return codigo;
	}
	/**
	 * @param codigo The codigo to set.
	 */
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	/**
	 * @return Returns the nombreFantasia.
	 */
	public String getNombrePais() {
		return nombrePais;
	}
	/**
	 * @param nombreFantasia The nombreFantasia to set.
	 */
	public void setNombrePais(String nombreFantasia) {
		this.nombrePais = nombreFantasia;
	}
}
