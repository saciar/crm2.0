package crm.gui.tablerenderer.abms;

public class BusquedaPartidosItem extends BusquedaItem{
	private String nombrePais;
	private String nombreProvincia;
	private String nombrePartido;
	private String idPartido;
	/**
	 * @return Returns the nombreProvincia.
	 */
	public String getNombreProvincia() {
		return nombreProvincia;
	}
	/**
	 * @param nombreProvincia The nombreProvincia to set.
	 */
	public void setNombreProvincia(String nombreProvincia) {
		this.nombreProvincia = nombreProvincia;
	}

	/**
	 * @return Returns the nombreFantasia.
	 */
	public String getNombrePais() {
		return nombrePais;
	}
	/**
	 * @param nombreFantasia The nombreFantasia to set.
	 */
	public void setNombrePais(String nombreFantasia) {
		this.nombrePais = nombreFantasia;
	}
	/**
	 * @return Returns the nombrePartido.
	 */
	public String getNombrePartido() {
		return nombrePartido;
	}
	/**
	 * @param nombrePartido The nombrePartido to set.
	 */
	public void setNombrePartido(String nombrePartido) {
		this.nombrePartido = nombrePartido;
	}
	/**
	 * @return Returns the idPartido.
	 */
	public String getIdPartido() {
		return idPartido;
	}
	/**
	 * @param idPartido The idPartido to set.
	 */
	public void setIdPartido(String idPartido) {
		this.idPartido = idPartido;
	}
}
